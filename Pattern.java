    public static void solidrhombus(int n){

        for (int i=1; i<=n;i++){
            for (int J=1;J<=n-i;J++){
                    System.out.print(" ");
                }
                for (int J=1;J<=n;J++){
                    System.out.print("*");
        }
        System.out.println();
    }
    }
